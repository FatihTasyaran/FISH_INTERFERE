def create_graph_add_vertical_relations(executors, nodes, entities, functions):
    """Build nx.DiGraph with all vertices and vertical (hierarchy) edges from Z_v sets.

    Vertical edges represent containment:
      EX → N  (executor contains node)
      N  → E  (node contains entity)
      E  → F  (entity contains function)

    Each nx node stores the FishVertex as attribute 'v'.
    Each vertical edge has attribute rel='contains'.
    """
    G = nx.DiGraph()

    # Add all vertices
    for ex in executors.values():
        G.add_node(ex.id_v, v=ex)
    for n in nodes.values():
        G.add_node(n.id_v, v=n)
    for e in entities.values():
        G.add_node(e.id_v, v=e)
    for f in functions.values():
        G.add_node(f.id_v, v=f)

    # Add vertical edges from Z_v membership sets
    for ex in executors.values():
        for n_id in ex.Z_v:
            G.add_edge(ex.id_v, n_id, rel="contains", level="L0_L1")

    for n in nodes.values():
        for e_id in n.Z_v:
            if e_id in entities:
                G.add_edge(n.id_v, e_id, rel="contains", level="L1_L2")

    for e in entities.values():
        for f_id in e.Z_v:
            if f_id in functions:
                G.add_edge(e.id_v, f_id, rel="contains", level="L2_L3")

    v_count = G.number_of_nodes()
    e_count = G.number_of_edges()
    print(f"[FISH graph] Vertical: {v_count} vertices, {e_count} containment edges")
    return G


def get_node_infos(mongo):
    """Load node_info from MongoDB into a dict keyed by node full_name.

    Returns:
      {"/kiss_icp_node": {"Subscribers": {topic: msg_type, ...},
                          "Publishers": {topic: msg_type, ...},
                          "Service_Servers": {name: type, ...},
                          "Service_Clients": {name: type, ...}},
       ...}
    """
    node_infos = {}
    for doc in mongo["node_info"].find():
        name = doc["node"]
        node_infos[name] = {
            "Subscribers": doc.get("Subscribers", {}),
            "Publishers": doc.get("Publishers", {}),
            "Service_Servers": doc.get("Service_Servers", {}),
            "Service_Clients": doc.get("Service_Clients", {}),
        }
    print(f"[FISH graph] Loaded node_info for {len(node_infos)} nodes")
    return node_infos


def _propagate_l3(G, src_entity_id, dst_entity_id, edge_attrs, entities, functions):
    """Propagate an entity-level edge down to function level.

    If both entities have function children, create edges between them
    carrying the same edge nature (msg/dep).
    """
    src_e = entities.get(src_entity_id)
    dst_e = entities.get(dst_entity_id)
    if not src_e or not dst_e:
        return 0

    src_funcs = [fid for fid in src_e.Z_v if fid in functions]
    dst_funcs = [fid for fid in dst_e.Z_v if fid in functions]

    count = 0
    for sf in src_funcs:
        for df in dst_funcs:
            G.add_edge(sf, df, **edge_attrs, level="L3")
            count += 1
    return count


def _aggregate_l1(G, nodes, entities):
    """Aggregate entity-level horizontal edges up to node level.

    For each pair of nodes that have entity-level edges between them,
    create a single node-level edge carrying the set of topics/services.
    """
    # Build entity_id → node_id reverse map
    entity_to_node = {}
    for n in nodes.values():
        for e_id in n.Z_v:
            if e_id in entities:
                entity_to_node[e_id] = n.id_v

    # Collect entity edges grouped by (src_node, dst_node)
    node_pairs = {}  # (src_node_id, dst_node_id) → list of edge info
    for u, v, data in G.edges(data=True):
        if data.get("level") != "L2":
            continue
        src_node = entity_to_node.get(u)
        dst_node = entity_to_node.get(v)
        if src_node is None or dst_node is None:
            continue
        key = (src_node, dst_node)
        node_pairs.setdefault(key, []).append(data)

    count = 0
    for (src_n, dst_n), edge_list in node_pairs.items():
        topics = [e.get("topic", e.get("service", "?")) for e in edge_list]
        nature_set = {e.get("nature", "?") for e in edge_list}
        G.add_edge(src_n, dst_n, rel="comm", level="L1",
                   comm_count=len(edge_list),
                   topics=topics,
                   natures=nature_set)
        count += 1
    return count


def _aggregate_l0(G, executors, nodes):
    """Aggregate node-level horizontal edges up to executor level.

    For each pair of executors that have node-level edges between them,
    create a single executor-level edge with total comm count.
    """
    # Build node_id → executor_id reverse map
    node_to_exec = {}
    for ex in executors.values():
        for n_id in ex.Z_v:
            node_to_exec[n_id] = ex.id_v

    # Collect node edges grouped by (src_exec, dst_exec)
    exec_pairs = {}  # (src_ex, dst_ex) → list of edge info
    for u, v, data in G.edges(data=True):
        if data.get("level") != "L1":
            continue
        src_ex = node_to_exec.get(u)
        dst_ex = node_to_exec.get(v)
        if src_ex is None or dst_ex is None:
            continue
        key = (src_ex, dst_ex)
        exec_pairs.setdefault(key, []).append(data)

    count = 0
    for (src_ex, dst_ex), edge_list in exec_pairs.items():
        total_comm = sum(e.get("comm_count", 1) for e in edge_list)
        all_topics = []
        all_natures = set()
        for e in edge_list:
            all_topics.extend(e.get("topics", []))
            all_natures.update(e.get("natures", set()))

        tau = "IntraP" if src_ex == dst_ex else "InterP"
        G.add_edge(src_ex, dst_ex, rel="comm", level="L0",
                   tau=tau,
                   comm_count=total_comm,
                   topics=all_topics,
                   natures=all_natures)
        count += 1
    return count


def add_horizontal_relations(G, node_infos, executors, nodes, entities, functions):
    """Add horizontal edges using entity aspects, then aggregate upward.

    Phase 2 edge construction — all vertices and vertical edges must be complete.

    L2 (entity level):
      - pub aspect on entity A → sub aspect on entity B (nature="msg")
      - cli aspect on entity A → req_sub aspect on entity B (nature="comm", request)
      - res_pub aspect on entity B → entity A (nature="dep", response)
      Fallback: unattributed pub/cli aspects use node-level matching

    L3 (function level): propagate L2 edges down to functions
    L1 (node level): aggregate L2 edges up to nodes
    L0 (executor level): aggregate L1 edges up to executors
    """
    # --- Topic metadata (msg type, hz) for edge attributes ---
    topic_meta = {}  # topic → {type, hz, ...}
    for doc in mongo["topic_info"].find():
        t = doc.get("topic")
        if t:
            topic_meta.setdefault(t, {})["msg_type"] = doc.get("type", "")
            topic_meta[t]["pub_count"] = doc.get("publisher_count", 0)
            topic_meta[t]["sub_count"] = doc.get("subscription_count", 0)
    for doc in mongo["topic_hz"].find():
        t = doc.get("topic")
        if t:
            topic_meta.setdefault(t, {})["avg_rate_hz"] = doc.get("average_rate", 0)
            topic_meta[t]["std_dev"] = doc.get("std_dev", 0)

    # --- Build indexes ---

    # sub aspect index: topic → [(entity_id, node_id), ...]
    sub_index = {}
    # srv (req_sub) aspect index: service_name → [(entity_id, node_id), ...]
    srv_index = {}
    # pub aspect index (entity-level): topic → [(entity_id, node_id), ...]
    pub_entity_index = {}

    # entity → parent node mapping
    entity_to_node = {}

    for n in nodes.values():
        for e_id in n.Z_v:
            e = entities.get(e_id)
            if not e or e.t_v != "E":
                continue
            entity_to_node[e_id] = n.id_v
            for aspect in e.A_v.get("aspects", []):
                if aspect["aspect"] == "sub":
                    topic = aspect["topic"]
                    sub_index.setdefault(topic, []).append((e_id, n.id_v))
                elif aspect["aspect"] == "req_sub":
                    srv = aspect["service"]
                    srv_index.setdefault(srv, []).append((e_id, n.id_v))
                elif aspect["aspect"] == "pub":
                    topic = aspect["topic"]
                    pub_entity_index.setdefault(topic, []).append((e_id, n.id_v))

    # Node-level pub index (for skipped edge counting only)
    pub_node_index = {}  # topic → [node_id, ...]
    for n in nodes.values():
        for pa in n.A_v.get("pub_aspects", []):
            pub_node_index.setdefault(pa["topic"], []).append(n.id_v)

    # cli aspect index: service → [(entity_id, node_id), ...] from attributed entities
    cli_entity_index = {}
    for n in nodes.values():
        for e_id in n.Z_v:
            e = entities.get(e_id)
            if not e or e.t_v != "E":
                continue
            for aspect in e.A_v.get("aspects", []):
                if aspect["aspect"] == "cli":
                    srv = aspect["service"]
                    cli_entity_index.setdefault(srv, []).append((e_id, n.id_v))

    # Node-level cli index (for skipped edge counting only)
    cli_node_index = {}  # service → [node_id, ...]
    for n in nodes.values():
        for ca in n.A_v.get("cli_aspects", []):
            cli_node_index.setdefault(ca["service"], []).append(n.id_v)

    def _topic_edge_attrs(topic, **extra):
        """Build edge attributes for a topic edge, including metadata."""
        attrs = {"rel": "comm", "level": "L2", "nature": "msg", "topic": topic}
        meta = topic_meta.get(topic, {})
        if meta.get("msg_type"):
            attrs["msg_type"] = meta["msg_type"]
        if meta.get("avg_rate_hz"):
            attrs["avg_rate_hz"] = meta["avg_rate_hz"]
        if meta.get("std_dev"):
            attrs["std_dev"] = meta["std_dev"]
        attrs.update(extra)
        return attrs

    # --- L2: Entity-level edges ---
    l2_count = 0

    # 1. Topic edges: pub aspect entity → sub aspect entity (same layer only)
    l2_skipped_pub = 0
    for topic, sub_list in sub_index.items():
        pub_entities = pub_entity_index.get(topic, [])
        if pub_entities:
            for pub_e_id, pub_n_id in pub_entities:
                for sub_e_id, sub_n_id in sub_list:
                    if pub_n_id == sub_n_id:
                        continue
                    G.add_edge(pub_e_id, sub_e_id, **_topic_edge_attrs(topic))
                    l2_count += 1
        else:
            # Unattributed pub — no edge (cross-layer edges not allowed)
            has_node_pub = bool(pub_node_index.get(topic, []))
            if has_node_pub and sub_list:
                l2_skipped_pub += 1

    # 2. Service edges: cli aspect entity → srv entity (same layer only)
    l2_skipped_cli = 0
    for srv_name, srv_list in srv_index.items():
        cli_entities = cli_entity_index.get(srv_name, [])
        if cli_entities:
            for cli_e_id, cli_n_id in cli_entities:
                for srv_e_id, srv_n_id in srv_list:
                    if cli_n_id == srv_n_id:
                        continue
                    G.add_edge(cli_e_id, srv_e_id, rel="comm", level="L2",
                               nature="comm", service=srv_name, direction="request")
                    G.add_edge(srv_e_id, cli_e_id, rel="comm", level="L2",
                               nature="dep", service=srv_name, direction="response")
                    l2_count += 2
        else:
            # Unattributed cli — no edge (cross-layer edges not allowed)
            has_node_cli = bool(cli_node_index.get(srv_name, []))
            if has_node_cli and srv_list:
                l2_skipped_cli += 1

    if l2_skipped_pub or l2_skipped_cli:
        print(f"[FISH graph] L2 skipped (unattributed): {l2_skipped_pub} pub, {l2_skipped_cli} cli")

    # --- External boundary entities ---
    # Detect topics/services with one side inside trace, other side missing.
    # Create EXT placeholder entities + functions for the missing side.
    ext_count = 0

    # All topics that have a subscriber entity but no publisher entity
    all_pub_topics = set(pub_entity_index.keys()) | set(pub_node_index.keys())
    for topic, sub_list in sub_index.items():
        if topic in all_pub_topics:
            continue  # has a publisher (entity or node level)
        # External publisher → create ext entity with pub aspect → edge to sub entity
        ext_e_A = ret_A_dict("E", label=f"ext:{topic}", etype="sub")
        ext_e_A["external"] = True
        ext_e_A["aspects"] = [{"aspect": "pub", "topic": topic}]
        ext_e = FishVertex("E", next(vertex_counter), ext_e_A, [], 2)
        entities[ext_e.id_v] = ext_e
        G.add_node(ext_e.id_v, v=ext_e)
        # Ext function
        ext_f_A = ret_A_dict("F", label=f"ext:{topic}", ptype="ext")
        ext_f = FishVertex("F", next(vertex_counter), ext_f_A, [], 3)
        functions[ext_f.id_v] = ext_f
        ext_e.Z_v.append(ext_f.id_v)
        G.add_node(ext_f.id_v, v=ext_f)
        G.add_edge(ext_e.id_v, ext_f.id_v, rel="contains", level="V")
        # Edges to all subscribers
        for sub_e_id, sub_n_id in sub_list:
            G.add_edge(ext_e.id_v, sub_e_id, **_topic_edge_attrs(topic, external=True))
            l2_count += 1
            ext_count += 1

    # All topics that have a publisher entity but no subscriber entity
    all_sub_topics = set(sub_index.keys())
    for topic, pub_list in pub_entity_index.items():
        if topic in all_sub_topics:
            continue
        # External subscriber → create ext entity with sub aspect
        ext_e_A = ret_A_dict("E", label=f"ext:{topic}", etype="sub")
        ext_e_A["external"] = True
        ext_e_A["aspects"] = [{"aspect": "sub", "topic": topic}]
        ext_e = FishVertex("E", next(vertex_counter), ext_e_A, [], 2)
        entities[ext_e.id_v] = ext_e
        G.add_node(ext_e.id_v, v=ext_e)
        ext_f_A = ret_A_dict("F", label=f"ext:{topic}", ptype="ext")
        ext_f = FishVertex("F", next(vertex_counter), ext_f_A, [], 3)
        functions[ext_f.id_v] = ext_f
        ext_e.Z_v.append(ext_f.id_v)
        G.add_node(ext_f.id_v, v=ext_f)
        G.add_edge(ext_e.id_v, ext_f.id_v, rel="contains", level="V")
        for pub_e_id, pub_n_id in pub_list:
            G.add_edge(pub_e_id, ext_e.id_v, **_topic_edge_attrs(topic, external=True))
            l2_count += 1
            ext_count += 1

    # Services that have a server entity but no client entity
    for srv_name, srv_list in srv_index.items():
        has_cli = bool(cli_entity_index.get(srv_name)) or bool(cli_node_index.get(srv_name))
        if has_cli:
            continue
        # External client → create ext entity with cli aspect
        ext_e_A = ret_A_dict("E", label=f"ext:{srv_name}", etype="sub")
        ext_e_A["external"] = True
        ext_e_A["aspects"] = [{"aspect": "cli", "service": srv_name}]
        ext_e = FishVertex("E", next(vertex_counter), ext_e_A, [], 2)
        entities[ext_e.id_v] = ext_e
        G.add_node(ext_e.id_v, v=ext_e)
        ext_f_A = ret_A_dict("F", label=f"ext:{srv_name}", ptype="ext")
        ext_f = FishVertex("F", next(vertex_counter), ext_f_A, [], 3)
        functions[ext_f.id_v] = ext_f
        ext_e.Z_v.append(ext_f.id_v)
        G.add_node(ext_f.id_v, v=ext_f)
        G.add_edge(ext_e.id_v, ext_f.id_v, rel="contains", level="V")
        for srv_e_id, srv_n_id in srv_list:
            G.add_edge(ext_e.id_v, srv_e_id, rel="comm", level="L2",
                       nature="comm", service=srv_name, direction="request", external=True)
            G.add_edge(srv_e_id, ext_e.id_v, rel="comm", level="L2",
                       nature="dep", service=srv_name, direction="response", external=True)
            l2_count += 2
            ext_count += 2

    if ext_count:
        print(f"[FISH graph] L2 external boundary edges: {ext_count}")

    print(f"[FISH graph] L2 horizontal edges: {l2_count}")

    # --- L3: Propagate entity edges to function level ---
    # When both sides of an L2 edge are entities, propagate to their functions.
    l3_count = 0
    for u, v, data in list(G.edges(data=True)):
        if data.get("level") != "L2":
            continue
        # Both sides must be entities (not nodes)
        if u not in entities or v not in entities:
            continue
        src_funcs = [f_id for f_id in entities[u].Z_v if f_id in functions]
        dst_funcs = [f_id for f_id in entities[v].Z_v if f_id in functions]
        for sf in src_funcs:
            for df in dst_funcs:
                G.add_edge(sf, df, rel="comm", level="L3", **{
                    k: v for k, v in data.items() if k not in ("rel", "level")
                })
                l3_count += 1
    print(f"[FISH graph] L3 propagated edges: {l3_count}")

    # --- L1: Aggregate entity edges to node level ---
    # Resolve both sides of L2 edges to their parent nodes.
    node_pairs = {}
    for u, v, data in G.edges(data=True):
        if data.get("level") != "L2":
            continue
        src_node = u if u in nodes else entity_to_node.get(u)
        dst_node = entity_to_node.get(v, v if v in nodes else None)
        if src_node is None or dst_node is None:
            continue
        key = (src_node, dst_node)
        node_pairs.setdefault(key, []).append(data)

    l1_count = 0
    for (src_n, dst_n), edge_list in node_pairs.items():
        topics = [e.get("topic", e.get("service", "?")) for e in edge_list]
        nature_set = {e.get("nature", "?") for e in edge_list}
        G.add_edge(src_n, dst_n, rel="comm", level="L1",
                   comm_count=len(edge_list),
                   topics=topics,
                   natures=nature_set)
        l1_count += 1
    print(f"[FISH graph] L1 aggregated edges: {l1_count}")

    # --- L0: Aggregate node edges to executor level ---
    l0_count = _aggregate_l0(G, executors, nodes)
    print(f"[FISH graph] L0 aggregated edges: {l0_count}")

    total_h = l2_count + l3_count + l1_count + l0_count
    print(f"[FISH graph] Total horizontal edges: {total_h}")
    return G


