def ret_A_dict(t_v, pid= -1, mode= "NA", label= "NA", etype= "NA", cb_addr= "NA", ptype= "NA"):

    if(t_v == "EX"):
        return {"label": label, "pid": pid, "mode": mode}
    if(t_v == "N"):
        return {"label": label, "publishers": {} }##Publishers will later be added as attributes
    if(t_v == "E"):
        return {"label": label, "etype": etype, "cb_addr": cb_addr}
    if(t_v == "F"):
        return {"label": label, "ptype": ptype}
    else:
        print("Unknown vertex type in ret_A_dict():", t_v)
        exit(1)
    

