_PARAM_TOPICS = {"/parameter_events"}
_PARAM_SRV_SUFFIXES = (
    "/describe_parameters",
    "/get_parameter_types",
    "/get_parameters",
    "/list_parameters",
    "/set_parameters",
    "/set_parameters_atomically",
)

def _is_param_service(name):
    """Check if a topic or service name belongs to ROS 2 parameter service infrastructure."""
    if name in _PARAM_TOPICS:
        return True
    for suffix in _PARAM_SRV_SUFFIXES:
        if name.endswith(suffix):
            return True
    return False

