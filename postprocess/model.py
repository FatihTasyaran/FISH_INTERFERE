@dataclass
class FishVertex:
    t_v: str    #"EX" | "N" | "E" | "F"
    id_v: str   # unique identifier for vertex
    A_v: dict = field(default_factory=dict) #Attributes
    Z_v: list = field(default_factory=list) #membership set (children ids)
    level: int = 0 # l: 0=EX, 1=N, 2=E, 3=F 


@dataclass
class FishEdge:
    src: str
    dst: str
    tau: str # "InterP" | "IntraP"
    msgs: list = field(default_factory=list) 

