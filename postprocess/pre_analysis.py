def build_nsys_pid_map(conn):
    """Build globalPid → real pid lookup from PROCESSES table."""
    gpid_to_pid = {}
    try:
        for r in conn.execute("SELECT globalPid, pid FROM PROCESSES"):
            gpid_to_pid[r[0]] = r[1]
    except Exception:
        pass
    return gpid_to_pid


def resolve_gtid(gtid, gpid_to_pid):
    """Decompose nsys globalTid → (pid, tid) using PROCESSES lookup."""
    base = gtid & ~0xFFFFFF
    tid = gtid & 0xFFFFFF
    pid = gpid_to_pid.get(base, base)
    return (pid, tid)


def resolve_gpid(gpid, gpid_to_pid):
    """Decompose nsys globalPid → pid using PROCESSES lookup."""
    return gpid_to_pid.get(gpid, gpid)


