def shorten_kernel_name(name):
    """Shorten a CUDA kernel name to its meaningful core."""
    if not name:
        return "?"
    prev = None
    short = name
    while short != prev:
        prev = short
        short = re.sub(r'<[^<>]*>', '', short)
    short = re.sub(r'^\w+\s+(?=\w)', '', short)
    short = re.sub(r'\(.*\)$', '', short)
    parts = short.split("::")
    if len(parts) > 3:
        short = "::".join(parts[-3:])
    return short.strip() or name[:60]


