def event_histogram(db, out_dir):
    """ros2_trace event type histogram → ros2_event_type_hist.png"""
    pipeline = [
        {"$group": {"_id": "$event", "count": {"$sum": 1}}},
        {"$sort": {"count": -1}},
    ]
    results = list(db["ros2_trace"].aggregate(pipeline))

    for r in results:
        print(f"  {r['_id']:60s} {r['count']:>10,}")

    names = [r["_id"] for r in results]
    counts = [r["count"] for r in results]

    plt.figure(figsize=(14, max(6, len(names) * 0.35)))
    plt.barh(names[::-1], counts[::-1])
    plt.xlabel("Count")
    plt.title(f"ros2_trace event histogram — {db.name}")
    plt.tight_layout()
    plt.savefig(os.path.join(out_dir, "ros2_event_type_hist.png"), dpi=150)
    plt.close()
    print(f"  → ros2_event_type_hist.png")


def _scope_where(session: str = "", container: str = "") -> str:
    """Build a WHERE clause that restricts rows to one (session, container).

    The InfluxDB `fish` database is shared across all FISH sessions; every
    point carries `session` + `container` tags written by the ingester.
    Charts must filter by both or they'd aggregate across unrelated runs.
    """
    parts = []
    if session:
        parts.append(f"session='{session}'")
    if container:
        parts.append(f"container='{container}'")
    return (" WHERE " + " AND ".join(parts)) if parts else ""


def gpu_histogram(influx, out_dir, session: str = "", container: str = ""):
    """InfluxDB measurement counts → nsys_event_type_histogram.png"""
    measurements = ["gpu_kernels", "gpu_memcpy", "cuda_runtime", "nvtx_events", "gpu_mem_usage"]
    where = _scope_where(session, container)
    counts = {}
    for m in measurements:
        try:
            table = influx.query(f'SELECT COUNT(*) AS cnt FROM "{m}"{where}')
            cnt = table.column("cnt")[0].as_py()
            counts[m] = cnt
        except Exception as e:
            print(f"  {m}: query failed ({e})")
            counts[m] = 0

    for name, cnt in sorted(counts.items(), key=lambda x: -x[1]):
        print(f"  {name:30s} {cnt:>12,}")

    names = sorted(counts, key=lambda x: counts[x])
    vals = [counts[n] for n in names]

    plt.figure(figsize=(10, 5))
    plt.barh(names, vals)
    plt.xlabel("Count")
    plt.title(f"InfluxDB measurement histogram — {influx._database}")
    plt.tight_layout()
    plt.savefig(os.path.join(out_dir, "nsys_event_type_histogram.png"), dpi=150)
    plt.close()
    print(f"  → nsys_event_type_histogram.png")


def thread_activity_chart(db, out_dir):
    """Top event-producing threads + callbacks → event_producing_threads_callbacks_short.png"""
    coll = db["ros2_trace"]

    top_threads = list(coll.aggregate([
        {"$group": {"_id": {"pid": "$vpid", "tid": "$vtid"}, "count": {"$sum": 1}}},
        {"$sort": {"count": -1}},
        {"$limit": 10},
    ]))

    thread_keys = [(r["_id"]["pid"], r["_id"]["tid"]) for r in top_threads]
    thread_event_dist = {}
    for pid, tid in thread_keys:
        dist = list(coll.aggregate([
            {"$match": {"vpid": pid, "vtid": tid}},
            {"$group": {"_id": "$event", "count": {"$sum": 1}}},
            {"$sort": {"count": -1}},
            {"$limit": 10},
        ]))
        total = sum(d["count"] for d in dist)
        thread_event_dist[(pid, tid)] = [
            (d["_id"], d["count"], d["count"] / total * 100) for d in dist
        ]

    top_callbacks = list(coll.aggregate([
        {"$match": {"event": "ros2:callback_start"}},
        {"$group": {
            "_id": {
                "pid": "$vpid",
                "tid": "$vtid",
                "callback": "$payload.callback",
            },
            "count": {"$sum": 1},
        }},
        {"$sort": {"count": -1}},
        {"$limit": 10},
    ]))

    cb_symbols = {}
    cb_handles = list({r["_id"]["callback"] for r in top_callbacks if r["_id"].get("callback")})
    if cb_handles:
        regs = coll.find(
            {"event": "ros2:rclcpp_callback_register", "payload.callback": {"$in": cb_handles}},
            {"payload.callback": 1, "payload.symbol": 1},
        )
        for reg in regs:
            cb_symbols[reg["payload"]["callback"]] = reg["payload"].get("symbol", "?")

    # ===================== PLOT =====================
    fig = plt.figure(figsize=(18, 16))
    gs = fig.add_gridspec(3, 1, height_ratios=[1.2, 1, 0.8], hspace=0.35)

    ax_bar = fig.add_subplot(gs[0])
    labels = [f"{r['_id']['pid']}-{r['_id']['tid']}" for r in top_threads]
    counts = [r["count"] for r in top_threads]
    bars = ax_bar.barh(labels[::-1], counts[::-1])
    ax_bar.set_xlabel("Event count")
    ax_bar.set_title(f"Top 10 event-producing threads — {db.name}")
    for bar, c in zip(bars, counts[::-1]):
        ax_bar.text(bar.get_width() + max(counts) * 0.01, bar.get_y() + bar.get_height() / 2,
                    f"{c:,}", va="center", fontsize=8)

    ax_t1 = fig.add_subplot(gs[1])
    ax_t1.axis("off")
    ax_t1.set_title("Per-thread top event types", fontsize=11, pad=10)

    t1_rows = []
    for pid, tid in thread_keys:
        dist = thread_event_dist[(pid, tid)]
        top_evts = "  |  ".join(f"{name.replace('ros2:','')} {pct:.0f}%" for name, _, pct in dist[:5])
        t1_rows.append([f"{pid}-{tid}", top_evts])

    table1 = ax_t1.table(
        cellText=t1_rows,
        colLabels=["pid-tid", "top event types (name  %)"],
        loc="center", cellLoc="left",
    )
    table1.auto_set_font_size(False)
    table1.set_fontsize(8)
    table1.scale(1, 1.4)

    ax_t2 = fig.add_subplot(gs[2])
    ax_t2.axis("off")
    ax_t2.set_title("Top 10 event-producing callbacks", fontsize=11, pad=10)

    t2_rows = []
    for i, r in enumerate(top_callbacks, 1):
        pid = r["_id"]["pid"]
        tid = r["_id"]["tid"]
        cb = r["_id"].get("callback", "?")
        sym = shorten_symbol(cb_symbols.get(cb, "?"))
        t2_rows.append([str(i), f"{pid}-{tid}", sym, f"{r['count']:,}"])

    table2 = ax_t2.table(
        cellText=t2_rows,
        colLabels=["#", "pid-tid", "callback symbol", "count"],
        loc="center", cellLoc="left",
    )
    table2.auto_set_font_size(False)
    table2.set_fontsize(8)
    table2.scale(1, 1.4)

    plt.savefig(os.path.join(out_dir, "event_producing_threads_callbacks_short.png"), dpi=150)
    plt.close()
    print(f"  → event_producing_threads_callbacks_short.png")


def gpu_activity_chart(influx, out_dir, session: str = "", container: str = ""):
    """GPU kernel/memcpy/API breakdown → gpu_event_producing_executions.png"""

    where = _scope_where(session, container)

    try:
        kern = influx.query(
            'SELECT kernel_short_name, COUNT(*) AS cnt '
            'FROM gpu_kernels '
            f'{where} '
            'GROUP BY kernel_short_name '
            'ORDER BY cnt DESC '
            'LIMIT 15'
        )
        kern_names = [str(v) for v in kern.column("kernel_short_name").to_pylist()]
        kern_counts = [int(v) for v in kern.column("cnt").to_pylist()]
    except Exception as e:
        print(f"  gpu_kernels query failed: {e}")
        kern_names, kern_counts = [], []

    try:
        kern_dur = influx.query(
            'SELECT kernel_short_name, '
            '  COUNT(*) AS cnt, '
            '  SUM(duration_ns) AS total_ns, '
            '  AVG(duration_ns) AS avg_ns '
            'FROM gpu_kernels '
            f'{where} '
            'GROUP BY kernel_short_name '
            'ORDER BY total_ns DESC '
            'LIMIT 10'
        )
        dur_names = kern_dur.column("kernel_short_name").to_pylist()
        dur_cnts = kern_dur.column("cnt").to_pylist()
        dur_totals = kern_dur.column("total_ns").to_pylist()
        dur_avgs = kern_dur.column("avg_ns").to_pylist()
    except Exception as e:
        print(f"  gpu_kernels duration query failed: {e}")
        dur_names, dur_cnts, dur_totals, dur_avgs = [], [], [], []

    try:
        memcpy = influx.query(
            'SELECT "copyKind", '
            '  COUNT(*) AS cnt, '
            '  SUM(bytes) AS total_bytes, '
            '  SUM(duration_ns) AS total_ns '
            'FROM gpu_memcpy '
            f'{where} '
            'GROUP BY "copyKind" '
            'ORDER BY cnt DESC'
        )
        mc_kinds = memcpy.column("copyKind").to_pylist()
        mc_cnts = memcpy.column("cnt").to_pylist()
        mc_bytes = memcpy.column("total_bytes").to_pylist()
        mc_dur = memcpy.column("total_ns").to_pylist()
    except Exception as e:
        print(f"  gpu_memcpy query failed: {e}")
        mc_kinds, mc_cnts, mc_bytes, mc_dur = [], [], [], []

    try:
        api = influx.query(
            'SELECT api_name, COUNT(*) AS cnt, SUM(duration_ns) AS total_ns '
            'FROM cuda_runtime '
            f'{where} '
            'GROUP BY api_name '
            'ORDER BY cnt DESC '
            'LIMIT 10'
        )
        api_names = api.column("api_name").to_pylist()
        api_cnts = api.column("cnt").to_pylist()
        api_dur = api.column("total_ns").to_pylist()
    except Exception as e:
        print(f"  cuda_runtime query failed: {e}")
        api_names, api_cnts, api_dur = [], [], []

    # ===================== PLOT =====================
    fig = plt.figure(figsize=(20, 20))
    gs = fig.add_gridspec(4, 1, height_ratios=[1.2, 1, 0.6, 0.8], hspace=0.35)

    ax_bar = fig.add_subplot(gs[0])
    ax_bar.barh(kern_names[::-1], kern_counts[::-1])
    ax_bar.set_xlabel("Invocation count")
    ax_bar.set_title(f"Top 15 GPU kernels by invocation count — {influx._database}")
    if kern_counts:
        for i, (bar, c) in enumerate(zip(ax_bar.patches, kern_counts[::-1])):
            ax_bar.text(bar.get_width() + max(kern_counts) * 0.01,
                        bar.get_y() + bar.get_height() / 2,
                        f"{c:,}", va="center", fontsize=7)

    ax_t1 = fig.add_subplot(gs[1])
    ax_t1.axis("off")
    ax_t1.set_title("Top 10 GPU kernels by total duration", fontsize=11, pad=10)

    t1_rows = []
    for i in range(len(dur_names)):
        t_ms = int(dur_totals[i]) / 1e6
        a_us = int(dur_avgs[i]) / 1e3
        t1_rows.append([
            str(i + 1),
            str(dur_names[i]),
            f"{int(dur_cnts[i]):,}",
            f"{t_ms:,.1f} ms",
            f"{a_us:,.1f} us",
        ])

    if t1_rows:
        table1 = ax_t1.table(
            cellText=t1_rows,
            colLabels=["#", "kernel", "count", "total time", "avg time"],
            loc="center", cellLoc="left",
        )
        table1.auto_set_font_size(False)
        table1.set_fontsize(8)
        table1.scale(1, 1.5)

    ax_t2 = fig.add_subplot(gs[2])
    ax_t2.axis("off")
    ax_t2.set_title("GPU memcpy breakdown by copy kind", fontsize=11, pad=10)

    t2_rows = []
    for i in range(len(mc_kinds)):
        t_ms = int(mc_dur[i]) / 1e6
        mb = int(mc_bytes[i]) / (1024 * 1024)
        t2_rows.append([
            str(mc_kinds[i]),
            f"{int(mc_cnts[i]):,}",
            f"{mb:,.1f} MB",
            f"{t_ms:,.1f} ms",
        ])

    if t2_rows:
        table2 = ax_t2.table(
            cellText=t2_rows,
            colLabels=["copyKind", "count", "total bytes", "total time"],
            loc="center", cellLoc="left",
        )
        table2.auto_set_font_size(False)
        table2.set_fontsize(8)
        table2.scale(1, 1.5)

    ax_t3 = fig.add_subplot(gs[3])
    ax_t3.axis("off")
    ax_t3.set_title("Top 10 CUDA runtime API calls", fontsize=11, pad=10)

    t3_rows = []
    for i in range(len(api_names)):
        t_ms = int(api_dur[i]) / 1e6
        t3_rows.append([
            str(i + 1),
            str(api_names[i]),
            f"{int(api_cnts[i]):,}",
            f"{t_ms:,.1f} ms",
        ])

    if t3_rows:
        table3 = ax_t3.table(
            cellText=t3_rows,
            colLabels=["#", "API call", "count", "total time"],
            loc="center", cellLoc="left",
        )
        table3.auto_set_font_size(False)
        table3.set_fontsize(8)
        table3.scale(1, 1.5)

    plt.savefig(os.path.join(out_dir, "gpu_event_producing_executions.png"), dpi=150)
    plt.close()
    print(f"  → gpu_event_producing_executions.png")


def gpu_thread_activity_chart(session_dir, out_dir):
    """GPU thread activity from nsys SQLite → gpu_event_producing_threads.png"""
    sqlite_files = glob.glob(os.path.join(session_dir, "nsys", "*.sqlite"))
    if not sqlite_files:
        print("No nsys SQLite files found")
        return

    thread_counts = Counter()
    thread_type_counts = {}

    tables_with_gtid = [
        ("CUPTI_ACTIVITY_KIND_RUNTIME", "cuda_runtime"),
    ]
    tables_with_gpid = [
        ("CUPTI_ACTIVITY_KIND_KERNEL", "gpu_kernels"),
        ("CUPTI_ACTIVITY_KIND_MEMCPY", "gpu_memcpy"),
    ]

    for sf in sqlite_files:
        conn = sqlite3.connect(sf)
        conn.row_factory = sqlite3.Row
        gpid_to_pid = build_nsys_pid_map(conn)

        for table, label in tables_with_gtid:
            try:
                rows = conn.execute(
                    f"SELECT globalTid, COUNT(*) as cnt FROM {table} GROUP BY globalTid"
                ).fetchall()
            except Exception:
                continue
            for r in rows:
                pid, tid = resolve_gtid(r["globalTid"], gpid_to_pid)
                key = (pid, tid)
                thread_counts[key] += r["cnt"]
                if key not in thread_type_counts:
                    thread_type_counts[key] = Counter()
                thread_type_counts[key][label] += r["cnt"]

        for table, label in tables_with_gpid:
            try:
                rows = conn.execute(
                    f"SELECT globalPid, COUNT(*) as cnt FROM {table} GROUP BY globalPid"
                ).fetchall()
            except Exception:
                continue
            for r in rows:
                pid = resolve_gpid(r["globalPid"], gpid_to_pid)
                key = (pid, "GPU")
                thread_counts[key] += r["cnt"]
                if key not in thread_type_counts:
                    thread_type_counts[key] = Counter()
                thread_type_counts[key][label] += r["cnt"]

        try:
            rows = conn.execute(
                "SELECT globalTid, COUNT(*) as cnt FROM NVTX_EVENTS GROUP BY globalTid"
            ).fetchall()
            for r in rows:
                pid, tid = resolve_gtid(r["globalTid"], gpid_to_pid)
                key = (pid, tid)
                thread_counts[key] += r["cnt"]
                if key not in thread_type_counts:
                    thread_type_counts[key] = Counter()
                thread_type_counts[key]["nvtx_events"] += r["cnt"]
        except Exception:
            pass

        conn.close()

    top10 = thread_counts.most_common(10)

    # ===================== PLOT =====================
    fig = plt.figure(figsize=(18, 12))
    gs = fig.add_gridspec(2, 1, height_ratios=[1.2, 1], hspace=0.3)

    ax_bar = fig.add_subplot(gs[0])
    labels = [f"{pid}-{tid}" for (pid, tid), _ in top10]
    counts = [c for _, c in top10]
    ax_bar.barh(labels[::-1], counts[::-1])
    ax_bar.set_xlabel("Event count")
    ax_bar.set_title("Top 10 GPU event-producing threads (from nsys SQLite)")
    if counts:
        for bar, c in zip(ax_bar.patches, counts[::-1]):
            ax_bar.text(bar.get_width() + max(counts) * 0.01,
                        bar.get_y() + bar.get_height() / 2,
                        f"{c:,}", va="center", fontsize=8)

    ax_t = fig.add_subplot(gs[1])
    ax_t.axis("off")
    ax_t.set_title("Per-thread GPU event type breakdown", fontsize=11, pad=10)

    t_rows = []
    for (pid, tid), total in top10:
        dist = thread_type_counts[(pid, tid)]
        parts = []
        for etype, cnt in dist.most_common(5):
            pct = cnt / total * 100
            parts.append(f"{etype} {pct:.0f}%")
        t_rows.append([f"{pid}-{tid}", f"{total:,}", "  |  ".join(parts)])

    table = ax_t.table(
        cellText=t_rows,
        colLabels=["pid-tid", "total", "event types (name  %)"],
        loc="center", cellLoc="left",
    )
    table.auto_set_font_size(False)
    table.set_fontsize(8)
    table.scale(1, 1.5)

    plt.savefig(os.path.join(out_dir, "gpu_event_producing_threads.png"), dpi=150)
    plt.close()
    print(f"  → gpu_event_producing_threads.png")


